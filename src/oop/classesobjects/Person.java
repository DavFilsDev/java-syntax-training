package oop.classesobjects;

/**
 * Represents a person with a name and an age.
 * This class helps demonstrate how to define a class and create objects in Java.
 */
public class Person {
    // Fields (attributes or properties)
    String name;
    int age;

    // Method to display person's information
    void introduce() {
        System.out.println("Hi, my name is " + name + " and I am " + age + " years old.");
    }
}
